@dataclass(init=True, frozen=True)
class Capacity:
    """A class to hold the capacity for a country to apply each measure."""

    country: str
    data: dict[str, dict[str, Literal[1, 2, 3]]]

    def __post_init__(self) -> None:

        for crs, data in self.data.items():
            v_2020 = data[BASELINE]
            v_2030 = data[Y_2030]
            v_2040 = data[Y_2040]
            v_2050 = data[Y_2050]

            assert (
                v_2020 <= v_2030 <= v_2040 <= v_2050
            ), f"Capacity should only ever increase but decreases for CRS {crs}! " + " <= ".join(
                str(v) for v in (v_2020, v_2030, v_2040, v_2050)
            )

    def __str__(self) -> str:
        string = f"{self.country}:"
        for crs, data in self.data.items():
            string += (
                f"\n\t{crs}: 2020: {data[BASELINE]} 2030: {data[Y_2030]} 2040: {data[Y_2040]} 2050: {data[Y_2050]}"
            )

        return string


@dataclass(init=True, frozen=True)
class Commitment:
    """A class to hold the levels of application of each measure."""

    name: str
    data: dict[str, float]

    def get_commitment(self, crs: str) -> float:
        """This function gets the commitment to a Carbon reduction strategy."""
        if crs in self.data:
            return self.data[crs]

        trimmed_crs = crs

        while len(trimmed_crs) > 0:

            trimmed_crs = trimmed_crs[0 : trimmed_crs.rfind(".")]

            if trimmed_crs in self.data:
                return self.data[trimmed_crs]

        raise KeyError(crs)

    def strategy_string(self, strategy: str) -> str:
        """This function gets the strategy string for a strategy."""
        matching_measures = {crs: value for crs, value in self.data.items() if crs.split(".", 1)[0] == strategy}

        if "9.12" in matching_measures and self.name != "APOLA" and matching_measures["9.12"] == 0.0:
            collect_warning("Removing 9.12 from strategy string as it should only be applied for APOLA!")
            matching_measures.pop("9.12")

        assert len(matching_measures), f"Strategy {strategy} does not have any matching measures!"

        minimum = min(matching_measures.values())
        maximum = max(matching_measures.values())

        min_level = None
        for i in range(1, len(COMMITMENT_LEVELS) + 1):
            if COMMITMENT_LEVELS[str(i)] > minimum:
                break

            min_level = i

        max_level = None
        for i in range(len(COMMITMENT_LEVELS), 0, -1):
            if COMMITMENT_LEVELS[str(i)] < maximum:
                break

            max_level = i
        if max_level is None:
            max_level = 4

        assert min_level is not None and min_level < 6, f"{minimum} is not a valid Commitment level!"
        assert max_level is not None and max_level > 0, f"{maximum} is not a valid Commitment level!"

        assert min_level <= max_level, f"Something went wrong! min ({min_level}) is higher than max ({max_level})!"

        if min_level == max_level:
            return str(min_level)

        return f"{min_level}{max_level}"

    def __str__(self) -> str:
        string = f"{self.name}:" + f" {self.data[""]}" if "" in self.data else ""
        for crs, value in self.data.items():
            if crs == "":
                continue

            string += "\n" + "    " * (crs.count(".") + 1) + f"{crs}: {value}"

        return string


class Measure:
    """A class to hold a single Measure of a Strategy."""

    def __init__(
        self,
        crs: str,
        low: dict[str, float],
        medium: dict[str, float],
        high: dict[str, float],
        other_data: dict[str, str],
    ) -> None:
        self.crs = crs
        self.low_data = low
        self.medium_data = medium
        self.high_data = high

        # Other miscellanious data
        self.other_data: dict[str, Any] = {
            "implementation": other_data["implementation"],
            "stock_activity_type_code": try_parse_array_string(other_data["stock_activity_type_code"]),
            "building_use_subtype_code": try_parse_array_string(other_data["building_use_subtype_code"]),
            "element_class_generic_name": try_parse_array_string(other_data["element_class_generic_name"]),
            "material_name_JRC_CDW": try_parse_array_string(other_data["material_name_JRC_CDW"]),
            "LCS_EN15978": try_parse_array_string(other_data["LCS_EN15978"]),
            "amount_material_kg_per_building": parse_boolean(other_data["amount_material_kg_per_building"]),
            "activity_in_out": try_parse_array_string(other_data["activity_in_out"]),
            "techflow_name_mmg": try_parse_array_string(other_data["techflow_name_mmg"]),
            "ind": try_parse_array_string(other_data["ind"]),
            "CRS_impact": other_data["CRS_impact"],
        }

    @property
    def strategy_crs(self) -> str:
        """The top-level Carbon Reduction Strategy code."""
        return self.crs[: self.crs.index(".")]

    @property
    def crs_impact(self) -> float:
        """The top-level Carbon Reduction Strategy code."""
        crs_impact = self.other_data["CRS_impact"]
        assert isinstance(crs_impact, (float, int))
        return float(crs_impact)

    def get_value(
        self, capacity: Literal[1, 2, 3], year: Literal["2030", "2040", "2050"], commitment_modifier: float
    ) -> float:
        """This function gets the value for a year and intensity value."""
        match capacity:
            case 1:
                current = self.low_data[year]
            case 2:
                current = self.medium_data[year]
            case 3:
                current = self.high_data[year]

        maximum = max(current, self.high_data[year])
        return min(current * commitment_modifier, maximum)

    def is_submeasure(self, crs: str) -> bool:
        """This function checks if the measure is a submeasure of the given measure string."""
        crs_split = crs.split(".")
        og_crs_split = self.crs.split(".")

        if len(crs_split) > len(og_crs_split):
            return False

        for val1, val2 in zip(crs_split, og_crs_split):
            if val1 != val2:
                return False

        return True

    def is_preprocessing(self) -> bool:
        """This function checks if the measure is a preprocessing measure."""
        implementation = self.other_data["implementation"]
        assert isinstance(implementation, str)

        return implementation == "preprocessing"

    def is_not_implemented(self) -> bool:
        """This function checks if the strategy is not implemented."""
        should_be_not_implemented = self.crs in NOT_IMPLEMENTED_MEASURES

        has_any_data = any(
            value is not None
            for dataset in (
                self.low_data,
                self.medium_data,
                self.high_data,
                self.other_data,
            )
            for value in dataset.values()
        )

        if has_any_data and should_be_not_implemented:
            collect_warning(f"Measure {self.crs} is marked as not implemented but has data!")

        if not has_any_data and not should_be_not_implemented:
            collect_warning(f"Measure {self.crs} is marked as implemented but has no data!")

        return should_be_not_implemented


@dataclass(init=True, frozen=True)
class Strategy:
    """A class to hold data on the Generic Strategies."""

    crs: str
    measures: list[Measure]


class Scenario:
    """This holds the Scenario Data."""

    country: str
    start: int
    end: int
    step_size: int
    commitment: Commitment | None
    data: dict[str, dict[str, Any]]

    def __init__(self, country: str, start: int, end: int, step_size: int) -> None:
        self.country = country

        self.start = start
        self.end = end
        self.step_size = step_size

        self.commitment = None

        self.data = {}

        self.__name: str | None = None

    def apply_strategies(self, capacity: Capacity, commitment: Commitment, strategies: list[Strategy]) -> None:
        """Apply strategies?"""
        self.commitment = commitment

        for strategy in strategies:
            for measure in strategy.measures:
                self.apply_measure(measure, capacity)

    def apply_floor_area(
        self, inter_values: tuple[float, float, float], variable: str, invert_data: bool = False
    ) -> None:
        """This function applies the values passed to the scenario data."""
        assert variable not in self.data, f'"{variable}"" seems to already be in data! Panicking!'

        data: dict[str, Any] = interpolate_decades(*inter_values)
        assert_valid_data(data, minimum=0, maximum=100)

        if invert_data:
            data = {key: 100 - value for key, value in data.items()}

        data["implementation"] = "stock"
        self.data[variable] = data

    def apply_use_of_empty(self, inter_values: tuple[float, float, float], use: str) -> None:
        """This function applies the values passed to the scenario data."""
        variable = "Use of empty"

        current_data = self.data.setdefault(variable, {"implementation": "stock"})

        data = interpolate_decades(*inter_values)
        assert_valid_data(data, minimum=0, maximum=100)

        index = {"empty dwellings": 0, "secondary dwellings": 1, "empty units": 2}[use]

        for year, value in data.items():
            year_data: tuple[int, int, int] = current_data.setdefault(year, (0, 0, 0))

            assert year_data[index] == 0, f'"{variable}" for {use} seems to already be in data! Panicking!'

            current_data[year] = year_data[:index] + (value,) + year_data[index + 1 :]

    def apply_refurbishment_rate(self, inter_values: tuple[float, float, float], residential: bool, level: str) -> None:
        """This function applies the values passed to the scenario data."""
        variable = f"Share of {"" if residential else "Non-"}Residential Refurbishments"

        current_data = self.data.setdefault(variable, {"implementation": "stock"})

        base_value = load_std_refurbishment_rates(self.country, residential=residential)[level] * 100

        data = interpolate_decades(*inter_values, baseline=base_value)
        assert_valid_data(data, minimum=0, maximum=100)

        for year, value in data.items():
            year_data: dict[str, float] = current_data.setdefault(year, {})

            assert year not in year_data, f'"{variable}"" seems to already be in data! Panicking!'

            year_data[level] = value

    def apply_share_of_new_conhea(
        self, inter_values: tuple[float, float, float], level: str, t_type: str, measure: Measure
    ) -> None:
        """This function applies a material shift to the scenario."""
        typologies = measure.other_data["building_use_subtype_code"]
        if isinstance(typologies, str):
            assert typologies == "*", f'stock_activity_type_code is invalid for measure {measure.crs}: "{typologies}"'
            typologies = TYPOLOGIES

        assert isinstance(typologies, list)

        data = interpolate_decades(*inter_values)
        assert_valid_data(data, minimum=0, maximum=100)

        for typology in typologies:
            variable = f"Share of new {typology} {t_type} {level}"
            assert variable not in self.data, f'"{variable}"" seems to already be in data! Panicking!'

            self.data[variable] = {"implementation": "stock"} | data

    def apply_share_of_energy_performance(
        self, inter_values: tuple[float, float, float], level: str, measure: Measure
    ) -> None:
        """This function applies a material shift to the scenario."""
        typologies = measure.other_data["building_use_subtype_code"]
        if isinstance(typologies, str):
            assert typologies == "*", f'stock_activity_type_code is invalid for measure {measure.crs}: "{typologies}"'
            typologies = TYPOLOGIES

        assert isinstance(typologies, list)

        baseline = load_energy_performance_data(self.country)[level] * 100

        data = interpolate_decades(*inter_values, baseline=baseline)
        assert_valid_data(data, minimum=0, maximum=100)

        for typology in typologies:
            variable = f"Share of new {typology} energy performance {level}"
            assert variable not in self.data, f'"{variable}"" seems to already be in data! Panicking!'

            self.data[variable] = {"implementation": "stock"} | data

    def apply_preprocessing(self, measure: Measure, inter_values: tuple[float, float, float]) -> None:
        """This function applies the values passed to the scenario data."""
        assert measure.crs not in self.data, f"Measure {measure.crs} seems to already be in data! Panicking!"

        if isnan(measure.crs_impact):
            collect_warning(f"Skipping measure {measure.crs} due to invalid CRS impact: {measure.crs_impact}")
            return

        multiplier = float(measure.crs_impact) / 10_000  # 100 * 100

        data = interpolate_decades(*inter_values)
        # assert_valid_data(data, minimum=0, maximum=100)

        self.data[measure.crs] = measure.other_data | {key: value * multiplier for key, value in data.items()}

    def apply_measure(self, measure: Measure, capacity: Capacity) -> None:
        """This function applies a measure to the Scenario"""
        assert self.commitment is not None, "Commitment is not set!"

        commitment_modifier = self.commitment.get_commitment(measure.crs)

        if commitment_modifier == 0:
            return

        if measure.crs == "9.12" and self.commitment.name != "APOLA":
            collect_warning(f"Measure {measure.crs} should only be applied for APOLA!")

        capacities = capacity.data

        inter_values = (
            measure.get_value(capacities[measure.strategy_crs][Y_2030], Y_2030, commitment_modifier),
            measure.get_value(capacities[measure.strategy_crs][Y_2040], Y_2040, commitment_modifier),
            measure.get_value(capacities[measure.strategy_crs][Y_2050], Y_2050, commitment_modifier),
        )

        match measure.crs:
            #
            # Floor area per person
            case "1.1":
                self.apply_floor_area(inter_values, "Floor area per person (NFA)", invert_data=True)
            #
            # Use of empty and secondary
            case "2.1":
                self.apply_floor_area(inter_values, "Share of buildings repurposed")
            case "2.2":
                self.apply_use_of_empty(inter_values, "empty dwellings")
                self.apply_use_of_empty(inter_values, "empty units")
            #
            # Timber construction
            case _ if measure.is_submeasure("4.1"):
                self.apply_share_of_new_conhea(inter_values, FULL_TIMBER, "construction", measure)
            case _ if measure.is_submeasure("4.2"):
                self.apply_share_of_new_conhea(inter_values, HYBRID_TIMBER, "construction", measure)
            #
            # Refurbishment rates
            case "9.1":
                self.apply_refurbishment_rate(inter_values, residential=True, level=LIGHT)
            case "9.2":
                self.apply_refurbishment_rate(inter_values, residential=True, level=MEDIUM)
            case "9.3":
                self.apply_refurbishment_rate(inter_values, residential=True, level=DEEP)
            case "9.4":
                self.apply_refurbishment_rate(inter_values, residential=False, level=LIGHT)
            case "9.5":
                self.apply_refurbishment_rate(inter_values, residential=False, level=MEDIUM)
            case "9.6":
                self.apply_refurbishment_rate(inter_values, residential=False, level=DEEP)
            #
            # Energy performance
            case _ if measure.crs in ("9.7", "9.8"):
                self.apply_share_of_energy_performance(inter_values, EP_NZEB, measure)
            #
            # Preprocessing
            case _ if measure.is_preprocessing():
                self.apply_preprocessing(measure, inter_values)
            case _:
                raise KeyError(f"Invalid measure found: {measure.crs}")

    @property
    def name(self) -> str:
        """The full formal name of the scenario"""
        if self.__name is None:
            assert self.commitment is not None

            strategy_strings: list[str] = []

            for i in range(1, NUM_STRATEGIES + 1):
                strategy_strings.append(self.commitment.strategy_string(str(i)))

            strategy_strings.append(self.commitment.strategy_string("4"))

            self.__name = f"{self.country}-{self.commitment.name}-{"-".join(strategy_strings)}"

        return self.__name

    def store(self, path: str) -> None:
        """This function stores the scenario to a specified File."""
        data = {
            "Name": self.name,
            "Model": "TUG",
            "Country": COUNTRY_CODE_TO_COUNTRY[self.country],
            "Start": self.start,
            "End": self.end,
            "Step size": self.step_size,
        } | self.data

        path = path.format(self.name)

        data_bytes = pickle.dumps(data)

        with open(path, "wb") as file:
            file.write(data_bytes)


