__COLLECTED_WARNINGS: dict[str, int] = {}


def collect_warning(message: str, no_count: bool = False) -> None:
    """This function colelcts the warnings into a file"""
    if no_count:
        if message not in __COLLECTED_WARNINGS:
            __COLLECTED_WARNINGS[message] = 1
    else:
        if message not in __COLLECTED_WARNINGS:
            __COLLECTED_WARNINGS[message] = 0

        __COLLECTED_WARNINGS[message] += 1


def clear_files(folder: str) -> None:
    """Clears the files generated by this script."""
    found_counter = 0

    tasks_path = TASKS_PATH.format(folder)

    if os.path.isfile(tasks_path):
        # print(f'Removing "{TASKS_FILE}".')
        os.remove(tasks_path)
        found_counter = found_counter + 1

    scenarios_folder = os.path.dirname(SCENARIO_PATH).format(folder)

    if os.path.isdir(scenarios_folder):
        found_counter += len(os.listdir(scenarios_folder))
        rmtree(scenarios_folder)

    if found_counter:
        print(f"Removed {found_counter} file{"s" if found_counter > 1 else ""}.")
    else:
        print("No files to clean.")


def print_collected_warnings() -> None:
    """This function prints all collected warnings"""

    for message, count in __COLLECTED_WARNINGS.items():
        if count > 1:
            print(f"{count}x ", end="")

        print(message)


def parse_boolean(string: str | None) -> bool | str | None:
    """Parses a string or None into a boolean."""
    if not isinstance(string, str):
        return string

    if string.lower() == "true":
        return True
    if string.lower() == "false":
        return False

    return string


def try_parse_array_string(string: str) -> list[str] | str:
    """This function tries to parse a string into an array. If that is not possible, the string gets returned"""
    try:
        result: list[str] = ast.literal_eval(string)
        assert isinstance(result, list)
        assert all(isinstance(item, str) for item in result)

        return result
    except (SyntaxError, ValueError):
        return string


