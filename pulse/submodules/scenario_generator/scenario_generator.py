def assert_valid_data(data: dict[str, int | float], minimum: int | float, maximum: int | float) -> None:
    """This function asserts that the data has the correct format."""
    all_years: list[str] = [str(year) for year in range(START_YEAR, END_YEAR + 1)]

    for year, value in data.items():
        if value > maximum:
            data[year] = maximum

    assert list(data.keys()) == all_years
    assert all(minimum <= value <= maximum for value in data.values())


def interpolate_decades(
    year_2030: float, year_2040: float, year_2050: float, baseline: float = 0.0
) -> dict[str, float]:
    """This function interpolates the years linearly."""
    result: dict[str, float] = {}

    for i in range(0, 11):
        result[str(2020 + i)] = baseline + (year_2030 - baseline) * i / 10

    for i in range(1, 11):
        result[str(2030 + i)] = year_2030 + (year_2040 - year_2030) * i / 10

    for i in range(1, 11):
        result[str(2040 + i)] = year_2040 + (year_2050 - year_2040) * i / 10

    return result


def get_extent_of_application(data: dict[str, str]) -> dict[str, Literal[1, 2, 3]]:
    """Gets the extent a country is capable of applying a Strategy."""

    def application_to_int(application: str) -> Literal[1, 2, 3]:
        """Application level to int."""

        match application:
            case _ if application in ("Low", "L"):
                return L_APPLICATION
            case _ if application in ("Medium", "M"):
                return M_APPLICATION
            case _ if application in ("High", "H"):
                return H_APPLICATION
            case _:
                raise KeyError(application)

    return {
        BASELINE: application_to_int(data[BASELINE]),
        Y_2030: application_to_int(data[Y_2030]),
        Y_2040: application_to_int(data[Y_2040]),
        Y_2050: application_to_int(data[Y_2050]),
    }


def get_base_capacities(capacities_path: str) -> list[Capacity]:
    """This function generated the Scenario versions"""
    capacities_df = read_dataframe_from_excel(capacities_path, "Capacities")

    capacities_df = capacities_df.rename({col: col.strip() for col in capacities_df.columns})
    capacities_df = capacities_df.with_columns(pl.col(pl.String).str.strip_chars())

    capacities: list[Capacity] = []

    for country in COUNTRY_CODE_LIST:
        relevant = capacities_df.filter(pl.col("country").str.contains(country, literal=True))

        if len(relevant) != NUM_STRATEGIES:
            found = len(relevant)
            print(f"Found {found} Strateg{"y" if found == 1 else "ies"} out of {NUM_STRATEGIES} for {country}!")
            continue

        capacity_data = {
            str(row["strategy_id"]): get_extent_of_application(row) for row in relevant.iter_rows(named=True)
        }

        capacities.append(Capacity(country, capacity_data))

    return capacities


def get_commitments(commitments_path: str) -> list[Commitment]:
    """This function generated the Scenario versions"""
    extension = os.path.splitext(commitments_path)[1]

    logging.getLogger("fastexcel.types.dtype").setLevel(logging.ERROR)

    if extension == ".xlsx":
        commitments_df = read_dataframe_from_excel(commitments_path, "Commitments")
    elif extension == ".parquet":
        commitments_df = pl.read_parquet(commitments_path)
    else:
        raise NotImplementedError(f'File extension "{extension}" is not supported!')

    schema: dict[str, pl.DataType] = {}

    for col, dtype in commitments_df.schema.items():
        if col in ("Scenario Name"):
            schema[col] = dtype
        else:
            schema[col] = pl.UInt16()

    commitments_df = pl.DataFrame(commitments_df, schema=schema)
    commitments_df = commitments_df.rename({col: " ".join(col.split()) for col in commitments_df.columns})
    commitments_df = commitments_df.with_columns(pl.col(pl.String).str.strip_chars())

    measures = [col for col in commitments_df.columns if col.startswith("CRS")]

    # Filling based on CRS hierarchy
    dependencies: dict[str, str] = {}

    for measure in measures:
        idx = measure.rfind(".")
        parent = measure[:idx] if idx != -1 else "Default"
        dependencies[measure] = parent

    dependencies = dict(sorted(dependencies.items(), key=lambda x: x[0].count(".")))
    relevant_cols = sorted(list(set(dependencies.keys()) - set(dependencies.values())))

    for comm, parent in dependencies.items():
        commitments_df = commitments_df.with_columns(
            pl.when(pl.col(comm).is_null()).then(pl.col(parent)).otherwise(pl.col(comm)).alias(comm)
        )

    commitments_df = commitments_df.select(["Scenario Name"] + relevant_cols)
    commitments_df = commitments_df.with_columns(pl.col(relevant_cols) / 100)
    commitments_df = commitments_df.rename({m: m[4:] for m in relevant_cols})

    base_commitments: list[Commitment] = []

    for data in commitments_df.iter_rows(named=True):
        base_commitments.append(Commitment(data.pop("Scenario Name"), data))

    return base_commitments


def get_strategies(path: str) -> list[Strategy]:
    """This function generated the Scenario versions"""
    strategies_df = read_dataframe_from_excel(path, sheet=None)

    strategies_df = strategies_df.rename({col: col.strip() for col in strategies_df.columns})
    strategies_df = strategies_df.with_columns(pl.col(pl.String).str.strip_chars())

    required_columns = {
        "CRS_code",
        "CRS_diffusion_2030_low",
        "CRS_diffusion_2030_medium",
        "CRS_diffusion_2030_high",
        "CRS_diffusion_2040_low",
        "CRS_diffusion_2040_medium",
        "CRS_diffusion_2040_high",
        "CRS_diffusion_2050_low",
        "CRS_diffusion_2050_medium",
        "CRS_diffusion_2050_high",
        "implementation",
        "stock_activity_type_code",
        "building_use_subtype_code",
        "element_class_generic_name",
        "material_name_JRC_CDW",
        "LCS_EN15978",
        "amount_material_kg_per_building",
        "activity_in_out",
        "techflow_name_mmg",
        "ind",
    }

    missing_cols = required_columns - set(strategies_df.columns)

    if missing_cols:
        raise ValueError(f"Strategies file is missing columns: {missing_cols}")

    strategies: list[Strategy] = []

    for crs in range(1, NUM_STRATEGIES + 1):
        regex = r"^" + str(crs) + r"\."

        relevant = strategies_df.filter(pl.col("CRS_code").str.contains(regex))

        if len(relevant) == 0:
            collect_warning(f"Found no Measures for Strategy {crs}!")
            continue

        measures: list[Measure] = [
            Measure(
                crs=row["CRS_code"],
                low={
                    Y_2030: row["CRS_diffusion_2030_low"],
                    Y_2040: row["CRS_diffusion_2040_low"],
                    Y_2050: row["CRS_diffusion_2050_low"],
                },
                medium={
                    Y_2030: row["CRS_diffusion_2030_medium"],
                    Y_2040: row["CRS_diffusion_2040_medium"],
                    Y_2050: row["CRS_diffusion_2050_medium"],
                },
                high={
                    Y_2030: row["CRS_diffusion_2030_high"],
                    Y_2040: row["CRS_diffusion_2040_high"],
                    Y_2050: row["CRS_diffusion_2050_high"],
                },
                other_data=row,
            )
            for row in relevant.iter_rows(named=True)
        ]

        measures = [measure for measure in measures if not measure.is_not_implemented()]

        strategies.append(Strategy(str(crs), measures))

    return strategies


def do_not_generate_data_for_country(country: str, countries: list[str]) -> bool:
    """Checks if the archetype stock data exists for the country."""
    generate_data = os.path.exists(ARCHETYPE_STOCK_DATA_PATH.format(country.lower()))

    generate_data &= country.upper() in countries

    return not generate_data


def create_and_store_scenarios(
    base_capacities: list[Capacity],
    commitments: list[Commitment],
    strategies: list[Strategy],
    args: SchedulerArguments,
) -> list[str]:
    """
    This function generates the Scenarios based on the given data.
    Returns the list of commitments that were generated.
    """

    tasks = TaskList(args)
    print("[" + " " * (len(args.countries) + 1) + "]", end="\033[2G", flush=True)

    scenario_path = SCENARIO_PATH.format(args.output_folder, r"{}")

    for base_capacity in base_capacities:
        if do_not_generate_data_for_country(base_capacity.country, args.countries):
            continue

        scenario = Scenario(base_capacity.country, args.start_year, args.end_year, args.step_size)

        for commitment in commitments:
            new_scenario = copy.deepcopy(scenario)
            new_scenario.apply_strategies(base_capacity, commitment, strategies)

            tasks.add_scenario(new_scenario.name)
            new_scenario.store(scenario_path)

        print("#", end="", flush=True)

    tasks.store()
    print("#", flush=True)

    return list(set(commitment.name for commitment in commitments))


def generate_scenarios() -> None:
    """The main method that runs the generation."""
    args = schedulerArgs
    clear_files(args.output_folder)

    print(f"Measures {format_list(NOT_IMPLEMENTED_MEASURES)} are not implemented.")
    print("Calculating scenarios...")

    scenario_path = os.path.dirname(SCENARIO_PATH).format(args.output_folder)

    os.makedirs(scenario_path, exist_ok=True)

    comms = create_and_store_scenarios(
        get_base_capacities(args.capacities_file),
        get_commitments(args.commitments_file),
        get_strategies(args.strategies_file),
        args,
    )

    print_collected_warnings()

    print(f"Generated commitments: {format_list(comms, sort=True)}")
